"""
VieNeu-TTS Multi-Backend Comparison Benchmark

Runs benchmark across all available backends and compares results.
"""

import argparse
import json
import sys
from datetime import datetime
from pathlib import Path

import torch

from tts_test.benchmark_vietneu import (
    benchmark_vietneu,
    print_benchmark_table,
    print_summary,
    BenchmarkResult,
)


BACKENDS = ["standard", "lmdeploy", "torch-compile"]


def run_all_benchmarks(
    device: str = "cuda",
    iterations: int = 5,
    text: str = "Xin chao, day la bai kiem tra giong noi tieng Viet cua cac mo hinh TTS.",
    save_output: bool = False,
    backbone_repo: str = "pnnbao-ump/VieNeu-TTS",
    codec_repo: str = "neuphonic/neucodec",
) -> list[BenchmarkResult]:
    """Run benchmark for all backends and return results."""

    print(f"\n{'='*70}")
    print(f"VieNeu-TTS Multi-Backend Benchmark Comparison")
    print(f"{'='*70}")
    print(f"Backbone: {backbone_repo}")
    print(f"Codec: {codec_repo}")
    print(f"Device: {device}")
    print(f"Iterations: {iterations}")
    print(f"Timestamp: {datetime.now().isoformat()}")
    print()

    results = []
    backend_results = {}

    for backend in BACKENDS:
        print(f"\n{'='*70}")
        print(f"Running: {backend}")
        print(f"{'='*70}")

        result = benchmark_vietneu(
            device=device,
            backend=backend,
            iterations=iterations,
            text=text,
            save_output=f"bench_vietneu_{backend}_{datetime.now().strftime('%Y%m%d_%H%M%S')}.wav" if save_output else None,
            backbone_repo=backbone_repo,
            codec_repo=codec_repo,
        )

        results.append(result)
        backend_results[backend] = result

    # Calculate speedup vs standard
    standard_result = backend_results.get("standard")
    if standard_result:
        for r in results:
            if r.backend != "standard" and standard_result.avg_inference_time_s > 0:
                r.speedup_vs_standard = round(
                    standard_result.avg_inference_time_s / r.avg_inference_time_s, 2
                )
            elif r.backend == "standard":
                r.speedup_vs_standard = 1.0

    return results


def write_summary(out_dir: Path, ts: str, runs: list[dict]):
    """Write summary markdown file."""
    lines = [f"# Benchmark Summary ({ts})", ""]
    lines.append("Generated by `benchmark_vietneu_all.py`.")
    lines.append("")
    lines.append("## Results")
    lines.append("")
    lines.append("| Run | Avg Time (s) | Audio Dur (s) | RTF | Peak GPU (MB) | Iters |")
    lines.append("|---|---:|---:|---:|---:|---:|")
    for r in runs:
        m = r.get("metrics", {}) or {}
        lines.append(
            f"| {r['name']} | "
            f"{m.get('avg_time_s', '')} | "
            f"{m.get('audio_dur_s', '')} | "
            f"{m.get('rtf', '')} | "
            f"{m.get('peak_gpu_mb', '')} | "
            f"{m.get('iters', '')} |"
        )
    lines.append("")
    lines.append("## Insights")
    lines.append("")
    lines.append("- Real-time threshold: `RTF < 1`")

    def _get(name: str, key: str):
        for r in runs:
            if r["name"] == name:
                return float((r.get("metrics", {}) or {}).get(key))
        return None

    gpu_std = _get("gpu_standard", "avg_time_s")
    gpu_lm = _get("gpu_lmdeploy", "avg_time_s")
    if gpu_std and gpu_lm:
        lines.append(f"- Speedup (LMDeploy vs Standard): ~`{gpu_std / gpu_lm:.1f}x`")
    lines.append("")

    summary_path = out_dir / f"summary_{ts}.md"
    summary_path.write_text("\n".join(lines) + "\n", encoding="utf-8", errors="replace")
    print(f"\nWrote summary: {summary_path}")


def main():
    parser = argparse.ArgumentParser(description="VieNeu-TTS All-Backends Benchmark")
    parser.add_argument("--device", type=str, default="cuda",
                       choices=["cuda", "cpu", "mps"], help="Device to use")
    parser.add_argument("--iters", type=int, default=5, help="Number of iterations")
    parser.add_argument("--text", type=str,
                       default="Xin chao, day la bai kiem tra giong noi tieng Viet cua cac mo hinh TTS.",
                       help="Text to synthesize")
    parser.add_argument("--backbone", type=str, default="pnnbao-ump/VieNeu-TTS",
                       help="Backbone model repository")
    parser.add_argument("--codec", type=str, default="neuphonic/neucodec",
                       help="Codec model repository")
    parser.add_argument("--save-output", action="store_true", help="Save output audio files")
    parser.add_argument("--out-dir", type=str, default="benchmark_runs", help="Output directory")
    parser.add_argument("--skip-lmdeploy", action="store_true", help="Skip LMDeploy run")
    parser.add_argument("--json", action="store_true", help="Output results as JSON")

    args = parser.parse_args()

    if args.device == "cuda" and not torch.cuda.is_available():
        print("CUDA not available, falling back to CPU")
        args.device = "cpu"

    # Update backends to run
    backends = ["standard", "torch-compile"]
    if not args.skip_lmdeploy:
        backends.insert(1, "lmdeploy")

    global BACKENDS
    BACKENDS = backends

    results = run_all_benchmarks(
        device=args.device,
        iterations=args.iters,
        text=args.text,
        save_output=args.save_output,
        backbone_repo=args.backbone,
        codec_repo=args.codec,
    )

    if args.json:
        from dataclasses import asdict
        print(json.dumps([asdict(r) for r in results], indent=2))
    else:
        print_benchmark_table(results)
        print_summary(results)

        # Write summary
        ts = datetime.now().strftime("%Y%m%d_%H%M%S")
        out_dir = Path(args.out_dir)
        out_dir.mkdir(parents=True, exist_ok=True)

        runs = []
        for r in results:
            runs.append({
                "name": f"gpu_{r.backend}",
                "metrics": {
                    "avg_time_s": r.avg_inference_time_s,
                    "audio_dur_s": r.audio_duration_s,
                    "rtf": r.rtf_avg,
                    "peak_gpu_mb": r.peak_gpu_memory_mb,
                    "iters": r.iterations,
                }
            })

        write_summary(out_dir, ts, runs)


if __name__ == "__main__":
    main()
